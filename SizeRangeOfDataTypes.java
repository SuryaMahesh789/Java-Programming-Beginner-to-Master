import java.lang.*;
import java.util.Scanner;
import java.util.*;


class SizeRangeOfDataTypes
{

    public static void main(String arg[])
    {
       
        System.out.println("Int Min : "+Integer.MIN_VALUE);
        System.out.println("Int Max : "+Integer.MAX_VALUE);
        System.out.println("Int Bytes : "+Integer.BYTES);
        System.out.println("Int Bytes : "+Integer.SIZE);
        
        
        System.out.println("Byte Min : "+Byte.MIN_VALUE);
        System.out.println("Byte Max : "+Byte.MAX_VALUE);
        System.out.println("Byte Bytes : "+Byte.BYTES);
        System.out.println("Byte Bytes : "+Byte.SIZE);
        
        
        System.out.println("Float Min : "+Float.MIN_VALUE);
        System.out.println("Float Max : "+Float.MAX_VALUE);
        System.out.println("Float Bytes : "+Float.BYTES);
        System.out.println("Float Bytes : "+Float.SIZE);
        
        
        System.out.println("Char Min : "+Character.MIN_VALUE);
        System.out.println("Char Max : "+Character.MAX_VALUE);
        System.out.println("Char Bytes : "+Character.BYTES);
        System.out.println("Char Bytes : "+Character.SIZE);
        

    }
}