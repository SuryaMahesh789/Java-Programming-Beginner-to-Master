public class IncDec {
    public static void main(String[] args) {
    int x=5;
    x++;
    System.out.println(x);
    
    x=5;
    ++x;
    System.out.println(x);
    
    x=5;
    int y=++x;
    System.out.println(y);
    
    x=5;
    y=x++;
    System.out.println(y);
    
    }
}
